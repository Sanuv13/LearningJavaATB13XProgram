package june19;

public class Lab002_operator {
    public static void main(String[] args) {
        int i=30;
        int j=20;
//airthmeic operator
        System.out.println(i+j);
        System.out.println(i-j);
        System.out.println(i*j);
        System.out.println(i/j);
        System.out.println(i%j);

        //assignment operator
        int a=10;
        System.out.println(a+=5);
        System.out.println(a-=5);
        System.out.println(a*=5);
        System.out.println(a/=5);
        System.out.println(a%=5);


    }
}
