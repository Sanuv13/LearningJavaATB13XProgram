package june12;

public class Lab001_operators {
    public static void main(String[] args) {
        //Arithmetic operator
        //+,-,/,%,8

        int A=10;
        int B=20;
        System.out.println(A+B);
        System.out.println(A-B);
        System.out.println(A*B);
        System.out.println(B/A);
        System.out.println(B%A);

        //Assigning operator
        //=
        int C=30;
        //Relational operator
        //> ,>=,<,<=,=,=!

        int i=50;
        int j=60;
        boolean k=i>j;
        boolean l=j>i;
        boolean m=i==j;
        boolean n=i>=j;

        System.out.println(k);
        System.out.println(l);
        System.out.println(m);
        System.out.println(n);

    }
}
